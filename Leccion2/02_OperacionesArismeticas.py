# Operaciones aritméticas
a = 10
b = 5

suma = a + b
print("Suma:", suma)

resta = a - b
print("Resta:", resta)

multiplicacion = a * b
print("Multiplicación:", multiplicacion)

division = a / b
print("División:", division)

division_entera = a // b
print("División Entera:", division_entera)

modulo = a % b
print("Módulo:", modulo)

exponente = a ** b
print("Exponenciación:", exponente)